   * бһ•бҹ’бһ‘бһ¶бҹҶбһ„бһ”бһүбҹ’бһҮбһ¶бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ”бһ„бҹ’бһҡбһҪбһҳбһ‘бҹҶбһ бҹҶбһҳбһҖбһҸбҹ’бһҡбһ№бһҳбһҸбҹӮ **300 \times 250** (бһҖбһ¶бһӣбһ–бһёбһҳбһ»бһ“бһ‘бҹҶбһ бҹҶ 520 \times 480 бһ’бҹҶбһҒбҹ’бһӣбһ¶бҹҶбһ„бһҺбһ¶бһҹбҹӢбһ”бһ¶бҹҶбһ„бһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбһ‘бһјбһҡбһҹбҹҗбһ–бҹ’бһ‘бһҮбһ·бһҸбһ–бһ¶бһҖбҹӢбһҖбһҺбҹ’бһҸбһ¶бһӣ)бҹ”
   * бһҡбһ…бһ“бһ¶бһЎбһҫбһ„бһңбһ·бһүбһҖбҹ’бһ“бһ»бһ„бһ‘бһҳбҹ’бһҡбһ„бҹӢбһ‘бһҳбҹ’бһ„бһ“бҹӢбһҹбҹ’бһҡбһ¶бһӣбһ”бҹҶбһ•бһ»бһҸ бһ„бһ¶бһҷбһҹбҹ’бһҡбһҪбһӣбһҳбһҫбһӣ бһҳбһ·бһ“бһүбҹүбҹҒбһҡбһүбҹүбҹғбһ—бҹ’бһ“бҹӮбһҖбҹ”
 3. **бһҮбһҳбҹ’бһҡбһ»бҹҮбһҡбһ¶бһӣбҹӢбһҖбҹҶбһ бһ»бһҹ Error бһӮбһ¶бҹҶбһ„бһҹбҹ’бһӮбҹ’бһҡбһёбһ” (Anti-Crash Mobile)бҹ–**
