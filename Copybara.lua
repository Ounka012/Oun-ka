   * бһҒбҹ’бһүбһ»бҹҶбһ”бһ¶бһ“бһ”бһ“бҹ’бһҗбҹӮбһҳ **бһ”бҹҠбһјбһҸбһ»бһ„бһҳбһјбһӣбһўбһҺбҹ’бһҸбҹӮбһҸбһ‘бһ№бһҖбһҸбһјбһ…бһҳбһҪбһҷ (Floating Icon рҹҰ«)** бһҠбҹӮбһӣбһўбҹ’бһ“бһҖбһўбһ¶бһ…бһҷбһҖбһҳбҹ’бһҡбһ¶бһҳбһҠбҹғбһўбһјбһҹбһ‘бҹ…бһҖбһ“бҹ’бһӣбҹӮбһ„бһҺбһ¶бһҖбҹҸбһ”бһ¶бһ“бҹ” бһ…бһ»бһ…бһӣбһҫбһңбһ¶бһҠбһҫбһҳбҹ’бһ”бһё бһӣбһ¶бһҖбҹӢ бһ¬ бһ”бһ„бҹ’бһ бһ¶бһүбһ•бҹ’бһ‘бһ¶бҹҶбһ„бһ”бһүбҹ’бһҮбһ¶бһ’бҹҶбһЎбһҫбһ„бһңбһ·бһүбһ”бһ¶бһ“бһӮбҹ’бһҡбһ”бҹӢбһ–бҹҒбһӣбһҷбҹүбһ¶бһ„бһ„бһ¶бһҷбһҹбҹ’бһҡбһҪбһӣбһ”бҹҶбһ•бһ»бһҸ!
